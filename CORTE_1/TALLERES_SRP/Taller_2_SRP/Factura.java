// Clase para hacer la Factura
class Factura {
    private int id;
    private double valor;
    
    public Factura(int id, double valor) {
        this.id = id;
        this.valor = valor;
    }

// Getters
    public int getId() {
        return id;
    }

    public double getValor() {
        return valor;
    }
}





